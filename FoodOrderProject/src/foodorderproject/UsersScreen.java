    public void findDynamically(String search){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        Users_Table.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }
    
    private void SearchBarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchBarKeyReleased
        String search = SearchBar.getText();
        findDynamically(search);      
    }//GEN-LAST:event_SearchBarKeyReleased

    
    
    public void showUsers(){
        model.setRowCount(0); 
        ArrayList<User> users = new ArrayList<User>();
        users = dbc.getTheUsers();
        if(users != null){
            for(User user: users ){
            Object[] add = {user.getId(),user.getName(),user.getSurname(),user.getMobile(),user.getAddress(),user.getPassword()};
            model.addRow(add);
            }
        }
    }
    
