    private void update_userActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_userActionPerformed
        // USER UPDATE BUTTON
       String name = name_area.getText();
       String surname = surname_area.getText();
       String mobile = mobile_area.getText();
       String address = address_area.getText();
       String password = password_area.getText();
       
       int selectedRow = Users_Table.getSelectedRow();
       if(selectedRow == -1){
           if(model.getRowCount() == 0){
               messeage_area.setText("No data");
           }else{
           messeage_area.setText("Please click one of the user to change.");
           }
       }else{
           int id = (int) model.getValueAt(selectedRow,0);
           dbc.updateUser(id,name,surname,mobile,address,password);
           showUsers();
           messeage_area.setText("User has successfully updated");
       }
    }//GEN-LAST:event_update_userActionPerformed

    private void delete_userActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_userActionPerformed
       messeage_area.setText("");
       int selectedRow = Users_Table.getSelectedRow();
       if(selectedRow == -1){
           if(model.getRowCount() == 0){
               messeage_area.setText("No data");
           }else{
           messeage_area.setText("Please click one of the user to delete.");
           }
       }else{
           int id = (int) model.getValueAt(selectedRow,0);
           dbc.deleteUser(id);
           showUsers();
           messeage_area.setText("User has successfully deleted.");
       }       
    }//GEN-LAST:event_delete_userActionPerformed

